package Java;

public class EscapeSequence {
    public static void main(String[] args) {
        // \t   It is going to leave paragraph space on the console

        System.out.println("\tThis is a tab space \tthis is      another one");
        System.out.println("this is a test \t");

        // \n   It is going to move on new line on the console
        System.out.print("\nNew line \ngjkdrglkdrjgk");
        System.out.println("Ziya");
        // the top is equal to the next one
        System.out.print("Ziya\n");

        // \"   prints double quote " on the console
        System.out.print("\nThis is \"ZIYA\"");

        // \\   prints \ on the console
        System.out.print("This  \\");

        // \'   prints single quote on the console
        System.out.print("\nThis is ''ZIYA'");
    }
}
